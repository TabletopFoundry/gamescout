const FIXED_PRICE_TIMESTAMP = "2025-03-01T12:00:00.000Z";

const RETAILER_URLS: Record<string, string> = {
  Amazon: "https://amazon.com",
  Target: "https://target.com",
  Walmart: "https://walmart.com",
  GameNerdz: "https://gamenerdz.com",
  "Miniature Market": "https://miniaturemarket.com",
  Cardhaus: "https://cardhaus.com",
  BestBuy: "https://bestbuy.com",
  BarnesAndNoble: "https://barnesandnoble.com",
};

type GameBlueprint = Omit<
  GameSeed,
  | "description"
  | "minPlayers"
  | "maxPlayers"
  | "minPlaytime"
  | "maxPlaytime"
  | "imageUrl"
  | "thumbnailUrl"
  | "prices"
> & {
  players: [number, number];
  playtime: [number, number];
  description?: string;
  basePrice: number;
  retailers?: string[];
  imageUrl?: string;
  thumbnailUrl?: string;
};

function describeGame(blueprint: GameBlueprint): string {
  const categoryText = blueprint.categories.slice(0, 2).join(" and ");
  const mechanicText = blueprint.mechanics.slice(0, 2).join(" plus ");
  return `${blueprint.name} is a ${blueprint.complexity >= 4 ? "heavy" : blueprint.complexity >= 3 ? "medium-heavy" : blueprint.complexity >= 2 ? "medium" : "light"} strategy game about ${categoryText.toLowerCase()} that leans on ${mechanicText.toLowerCase()}. It plays ${blueprint.players[0] === blueprint.players[1] ? `best at ${blueprint.players[0]}` : `from ${blueprint.players[0]} to ${blueprint.players[1]}`} players in roughly ${blueprint.playtime[0]}-${blueprint.playtime[1]} minutes and remains a staple recommendation for ${blueprint.publisher} fans.`;
}

function buildPrices(basePrice: number, retailers: string[] = ["Amazon", "GameNerdz", "Miniature Market"]) {
  const multipliers = [1.08, 0.94, 0.99, 1.02];
  return retailers.map((retailer, index) => {
    const multiplier = multipliers[index % multipliers.length] ?? 1;
    return {
      retailer,
      price: Number(Math.max(9.99, basePrice * multiplier).toFixed(2)),
      url: RETAILER_URLS[retailer] ?? "https://boardgamegeek.com",
      updatedAt: FIXED_PRICE_TIMESTAMP,
    };
  });
}

function makeGame(blueprint: GameBlueprint): GameSeed {
  return {
    id: blueprint.id,
    name: blueprint.name,
    year: blueprint.year,
    description: blueprint.description ?? describeGame(blueprint),
    minPlayers: blueprint.players[0],
    maxPlayers: blueprint.players[1],
    minPlaytime: blueprint.playtime[0],
    maxPlaytime: blueprint.playtime[1],
    complexity: blueprint.complexity,
    bggRating: blueprint.bggRating,
    bggRank: blueprint.bggRank,
    categories: blueprint.categories,
    mechanics: blueprint.mechanics,
    designer: blueprint.designer,
    publisher: blueprint.publisher,
    imageUrl: blueprint.imageUrl ?? DEFAULT_IMAGE_URL,
    thumbnailUrl: blueprint.thumbnailUrl ?? DEFAULT_THUMBNAIL_URL,
    prices: buildPrices(blueprint.basePrice, blueprint.retailers),
  };
}

